import React, { createContext, useContext, useState, ReactNode, useEffect } from 'react';
import { UserService } from '../services/userService';
import { unifiedSyncService } from '../services/unifiedSyncService';
import AsyncStorage from '@react-native-async-storage/async-storage';

interface UserInfo {
  id: string;
  nickname?: string;
  phone?: string;
  email?: string;
  avatar?: string;
  loginType?: 'wechat' | 'apple' | 'phone' | 'guest';
  createdAt?: string;
  updatedAt?: string;
  learningStats?: {
    level?: number;
    experience?: number;
    totalWords?: number;
    masteredWords?: number;
    learningDays?: number;
    currentStreak?: number;
    totalReviews?: number;
    accuracy?: number;
  };
}

interface AuthContextType {
  user: UserInfo | null;
  loginType: string | null;
  isAuthenticated: boolean;
  login: (userData: UserInfo, type: string) => Promise<void>;
  logout: () => Promise<void>;
  updateUser: (userData: Partial<UserInfo>) => void;
  getAuthToken: () => Promise<string | null>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const useAuth = () => {
  const ctx = useContext(AuthContext);
  if (!ctx) throw new Error('useAuth must be used within AuthProvider');
  return ctx;
};

export const AuthProvider = ({ children }: { children: ReactNode }) => {
  const [user, setUser] = useState<UserInfo | null>(null);
  const [loginType, setLoginType] = useState<string | null>(null);
  const [isAuthenticated, setIsAuthenticated] = useState(false);
  const userService = UserService.getInstance();

  // ÂàùÂßãÂåñÊó∂Âä†ËΩΩÁî®Êà∑‰ø°ÊÅØ
  useEffect(() => {
    loadUserInfo();
  }, []);

  const loadUserInfo = async () => {
    try {
      console.log('üîç AuthContext loadUserInfo ÂºÄÂßã');
      const userLoginInfo = await userService.getUserLoginInfo();
      if (userLoginInfo) {
        console.log('üîç AuthContext loadUserInfo ÊâæÂà∞Áî®Êà∑Êï∞ÊçÆ:', userLoginInfo);
        setUser(userLoginInfo.userData);
        setLoginType(userLoginInfo.loginType);
        setIsAuthenticated(true);
      } else {
        console.log('üîç AuthContext loadUserInfo Ê≤°ÊúâÊâæÂà∞Áî®Êà∑Êï∞ÊçÆ');
        
        // Ê£ÄÊü•ÊòØÂê¶ÊúâËá™Âä®ÁîüÊàêÁöÑÊ∏∏ÂÆ¢ID
        const autoGeneratedUserData = await AsyncStorage.getItem('userData');
        if (autoGeneratedUserData) {
          try {
            const parsedData = JSON.parse(autoGeneratedUserData);
            if (parsedData.isAutoGenerated && parsedData.loginType === 'guest') {
              console.log('üîç ÂèëÁé∞Ëá™Âä®ÁîüÊàêÁöÑÊ∏∏ÂÆ¢ID:', parsedData);
              setUser(parsedData);
              setLoginType('guest');
              setIsAuthenticated(false); // Ëá™Âä®ÁîüÊàêÁöÑÁî®Êà∑ÈúÄË¶ÅÊâãÂä®ÁôªÂΩïÊâçËÉΩËé∑Âæótoken
            }
          } catch (error) {
            console.error('‚ùå Ëß£ÊûêËá™Âä®ÁîüÊàêÁöÑÁî®Êà∑Êï∞ÊçÆÂ§±Ë¥•:', error);
          }
        }
      }
    } catch (error) {
      console.error('‚ùå AuthContext loadUserInfo Â§±Ë¥•:', error);
    }
  };

  const login = async (userData: UserInfo, type: string) => {
    try {
      console.log('üîê AuthContext login ÂºÄÂßã:', { userData, type });
      await userService.saveUserLoginInfo(userData, type);
      console.log('üîê AuthContext login ‰øùÂ≠òÂà∞Êú¨Âú∞Â≠òÂÇ®ÂÆåÊàê');
      
      setUser(userData);
      setLoginType(type);
      setIsAuthenticated(true);
      
      console.log('üîê AuthContext login Áä∂ÊÄÅÊõ¥Êñ∞ÂÆåÊàê:', { 
        user: userData, 
        loginType: type, 
        isAuthenticated: true 
      });
      // Êñ∞Â¢ûÔºöÁôªÂΩïÂêéÂêåÊ≠•Êú¨Âú∞ÂéÜÂè≤Âà∞‰∫ëÁ´Ø
      await unifiedSyncService.addToSyncQueue({
        type: 'searchHistory',
        data: await AsyncStorage.getItem('searchHistory') || [],
        userId: userData.id,
        operation: 'update',
        priority: 'high'
      });
    } catch (error) {
      console.error('‚ùå AuthContext login Â§±Ë¥•:', error);
      throw error;
    }
  };

  const logout = async () => {
    try {
      await userService.clearUserLoginInfo();
      setUser(null);
      setLoginType(null);
      setIsAuthenticated(false);
    } catch (error) {
      console.error('ÁôªÂá∫Â§±Ë¥•:', error);
    }
  };

  const updateUser = (userData: Partial<UserInfo>) => {
    if (user) {
      setUser({ ...user, ...userData });
    }
  };

  const getAuthToken = async (): Promise<string | null> => {
    try {
      return await userService.getAuthToken();
    } catch (error) {
      console.error('‚ùå AuthContext getAuthToken Â§±Ë¥•:', error);
      return null;
    }
  };

  return (
    <AuthContext.Provider value={{ user, loginType, isAuthenticated, login, logout, updateUser, getAuthToken }}>
      {children}
    </AuthContext.Provider>
  );
}; 